text = " Hola Mundo "

# Uppercase
print(text.upper())

# Lower
print(text.lower())

# Capitalize
print(text.capitalize())

# Strip
print(text.strip())

# Split
splited = text.split(' ')
print(splited)

# Join
joined_text = " ".join(splited)
print(joined_text)

# Replace
print(text.replace('Mundo', 'Cesar'))

# Find
print(text.find("Mundo"))

